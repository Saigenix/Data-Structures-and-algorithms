    int A[] = {1, 2, 5, 6, 12, 54, 625, 7, 23, 9, 987};
    int n = 11;
    int l = 0;
    int h = n- 1;
    printArray(A, n); // Printing the array before sorting
    bubbleSort(A, n);
    mergeSort(A,l,h);
    printArray(A, n); 
    quickSort(A,l,h);
     // Function to sort the array
